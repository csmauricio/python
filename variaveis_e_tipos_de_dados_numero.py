#Variáveis Numero

#Operações
valor = 2 + 3
print(valor)

valor = 3 - 2
print(valor)

valor = 2 * 3
print(valor)

valor = 2 ** 3
print(valor)

#no python2 a divisão dá sempre em inteiro, ao menos que um dos numeros seja decimal
valor = 3 / 2
print(valor)

valor = 2 + 3 * 4
print(valor)

valor = (2 + 3) * 4
print(valor)

#valores decimais
valor = 0.2 + 0.1
print(valor)

print(2 + 3)

#Converter em texto
idade = 23
message = "Feliz " + str(idade) + "° aniversário!"
print(message)
