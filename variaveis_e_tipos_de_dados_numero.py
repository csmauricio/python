print("\nOperações matemáticas")
print("\n2 + 3")
valor = 2 + 3
print(valor)

print("\n3 - 2")
valor = 3 - 2
print(valor)

print("\n2 - 3")
valor = 2 - 3
print(valor)

print("\n2 * 3")
valor = 2 * 3
print(valor)

print("\n2 ** 3")
valor = 2 ** 3
print(valor)

#no python2 a divisão dá sempre em inteiro, ao menos que um dos numeros seja decimal
print("\n3 / 2 (o python2 a divisão dá sempre em inteiro, ao menos que um dos numeros seja decimal)")
valor = 3 / 2
print(valor)

print("\n2 + 3 * 4")
valor = 2 + 3 * 4
print(valor)

print("\n(2 + 3) * 4")
valor = (2 + 3) * 4
print(valor)

